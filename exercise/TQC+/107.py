# 107_數值計算

# 題目說明:

# 請撰寫一程式，讓使用者輸入五個數字，計算並輸出這五個數字之數值、總和及平均數

# 提示： 輸出浮點數到小數點後第1位

a=eval(input("1："))
b=eval(input("2："))
c=eval(input("3："))
d=eval(input("4："))
e=eval(input("5："))

su=a+b+c+d+e
au=su/5

print("{} {} {} {} {}\n總和={:.1f}\n平均數={:.1f}".format(a,b,c,d,e,su,au))

# 範例輸入1

# 20
# 40
# 60
# 80
# 100
# 範例輸出1

# 20 40 60 80 100
# Sum = 300.0
# Average = 60.0
# 範例輸入2

# 88.7
# 12
# 56
# 132.55
# 3
# 範例輸出2

# 88.7 12 56 132.55 3
# Sum = 292.2
# Average = 58.5