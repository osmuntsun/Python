# 208_十進位換算

# 題目說明:

# 請使用選擇敘述撰寫一程式，讓使用者輸入一個十進位整數num(0 ≤ num ≤ 15)，將num轉換成十六進位值。

# 提示:轉換規則 = 十進位0~9的十六進位值為其本身，十進位10~15的十六進位值為A~F。 

num = int( input() )

if 10 <= num <= 15:
    print('{:x}'.format(num))
else:
    print( num )

# 範例輸入1

# 13
# 範例輸出1

# D
# 範例輸入2

# 8
# 範例輸出2

# 8

# 額外補充
