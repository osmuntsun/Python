# TQC+ 程式語言Python 410 繪製等腰三角形
# 設計說明：
# 請撰寫一程式，依照使用者輸入的n，畫出對應的等腰三角形。

n=eval(input("輸入N："))
for i in range(1,n+1):
    print(" "*(n-i)+"*"*(i*2-1),end="")
    print()


# 3. 輸入輸出：
# 輸入說明
# 一個正整數
# 輸出說明
# 以 * 畫出等腰三角形
# （每列最後一個 * 的右方無空白）
# 輸入輸出範例
# 範例輸入
# 7
# 範例輸出
#       *
#      ***
#     *****
#    *******
#   *********
#  ***********
# *************